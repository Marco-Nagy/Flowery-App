class SearchResponseEntity {
  final List<SearchResponseEntityProducts?>? products;

  SearchResponseEntity({this.products});
}

class SearchResponseEntityProducts {
  final String? id;
  final String? title;
  final String? slug;
  final String? description;
  final String? imgCover;
  final List<String?>? images;
  final int? price;
  final int? priceAfterDiscount;
  final int? quantity;
  final String? category;
  final String? occasion;
  final int? discount;
  final int? sold;

  SearchResponseEntityProducts({
    this.id,
    this.title,
    this.slug,
    this.description,
    this.imgCover,
    this.images,
    this.price,
    this.priceAfterDiscount,
    this.quantity,
    this.category,
    this.occasion,
    this.discount,
    this.sold,
  });
}
